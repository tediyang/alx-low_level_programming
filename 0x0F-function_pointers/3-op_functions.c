/**
 * op_add - adds a and b
 *
 * @a: param
 * @b: param
 *
 * Return: gives the sum of a and b.
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract a and b
 *
 * @a: param
 * @b: param
 *
 * Return: answer
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply a and b
 *
 * @a:param
 * @b: param
 *
 * Return: answer
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide a and b
 *
 * @a: param
 * @b: param
 *
 * Return: answer
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find modulus of a and b
 *
 * @a: param
 * @b: param
 *
 * Return: answer
 */

 int op_mod(int a, int b)
{
	return (a % b);
}
